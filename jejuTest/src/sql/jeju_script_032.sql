-- 숙소 예약 관리 뷰
CREATE OR REPLACE VIEW OWNERRESERVATION AS 
(SELECT ROWNUM RNUM, ROOM_NAME, RESERV_PAX, CHECKIN_DATE, CHECKOUT_DATE, RESERV_DATE, MEM_NAME, MEM_PHONE, RESERV_PRICE, OWNER_NUM
FROM RESERVATION
JOIN ROOM USING(ROOM_NUM)
JOIN ACCOMMODATION USING(ACM_NUM)
JOIN MEMBER USING(MEM_NUM));


--숙소 리스트 페이징 처리 뷰
CREATE OR REPLACE VIEW ACMLIST AS
SELECT * 
FROM( SELECT ROWnUM RNUM, A.*
        FROM(SELECT ACM_NUM,ACM_NAME,MEM_NAME,ACM_PHONE,ACM_TYPE,ACM_GRADE,ACM_POWER,ACM_STATUS,ACM_ADDRESS 
                FROM ACCOMMODATION A 
                JOIN MEMBER ON(A.OWNER_NUM = MEM_NUM)
                ORDER BY ACM_NUM DESC) A);
                
--어드민 인덱스 페이지 뷰
CREATE OR REPLACE VIEW ADMININDEX AS
SELECT * FROM(
SELECT (SELECT COUNT(*) FROM MEMBER WHERE MEM_STATUS = 'Y' AND MEM_TYPE='U')U_COUNT,
        (SELECT COUNT(*) FROM MEMBER WHERE MEM_STATUS = 'Y' AND MEM_TYPE='O')O_COUNT,
        (SELECT SUM(PAY_PRICE) FROM PAYMENT WHERE  PAY_CANCEL ='N')P_TOTAL
FROM DUAL WHERE ROWNUM=1);

--멤버 페이징 처리 뷰
CREATE OR REPLACE VIEW MEMLIST AS
SELECT *
FROM(SELECT ROWNUM RNUM, A.*
        FROM(SELECT * FROM MEMBER) A);

--결제 페이징처리뷰
CREATE OR REPLACE VIEW PAYMENTLIST AS
SELECT *
FROM(SELECT ROWNUM RNUM, A.*
        FROM(SELECT * FROM PAYMENT) A);


--리뷰 리스트 페이징 처리 뷰
CREATE OR REPLACE VIEW REVIEWLIST AS
SELECT * 
FROM (SELECT ROWNUM RNUM,A.*
        FROM (SELECT REVIEW_NUM, MEM_NAME,ACM_NAME,RESERVATION_NUM, REVIEW_SCORE,REVIEW_TITLE,REVIEW_CONTENT,REVIEW_DATE
            FROM REVIEW
            JOIN MEMBER USING(MEM_NUM)
            JOIN ACCOMMODATION USING(ACM_NUM)
            ORDER BY REVIEW_NUM DESC) A);

-- 예약 조회 페이징 처리 뷰
CREATE OR REPLACE VIEW RESERVLIST AS
SELECT * 
FROM(SELECT ROWNUM RNUM,A.*
        FROM (SELECT RESERV_NUM,MEM_NAME,ACM_NAME,ROOM_NAME,RESERV_PAX,TO_CHAR(CHECKIN_DATE),TO_CHAR(CHECKOUT_DATE),RESERV_PRICE,RESERV_CANCEL,TO_CHAR(RESERV_DATE),TO_CHAR(CANCEL_DATE),RESERV_REQUIRE,R.NOSHOW,R.STATUS
                FROM RESERVATION R
                JOIN MEMBER USING(MEM_NUM)
                JOIN ROOM USING(ROOM_NUM)
                JOIN ACCOMMODATION USING(ACM_NUM)
                ORDER BY TO_NUMBER(RESERV_NUM) DESC)A);

--파워 조회 페이징 처리 뷰
CREATE OR REPLACE VIEW POWERLIST AS
SELECT * FROM(
SELECT ROWNUM RNUM,A.*
FROM(SELECT POWER_NUM, ACM_NAME,PAY_NUM,TO_CHAR(START_DATE),TO_CHAR(END_DATE),POWER_STATUS,POWER_FLAG
        FROM POWER
        JOIN ACCOMMODATION USING(ACM_NUM)
        ORDER BY POWER_NUM DESC) A);



--CHART VIEW 최근 4개월 조회.
CREATE OR REPLACE VIEW CHARTVIEW1 AS
select * from (SELECT TO_NUMBER(TO_CHAR(RESERV_DATE,'MM')) 월별 , SUM(RESERV_PRICE) 금액
            FROM RESERVATION 
            GROUP BY TO_CHAR(RESERV_DATE,'MM') 
            ORDER BY TO_CHAR(RESERV_DATE,'MM'))
            where 월별 BETWEEN TO_NUMBER(TO_CHAR(ADD_MONTHS(SYSDATE,-3),'MM')) AND TO_NUMBER(TO_CHAR(SYSDATE,'MM'));


--최근 4개월간 숙소 유형별 매출
CREATE OR REPLACE VIEW CHARTVIEW2 AS
SELECT * FROM(SELECT ACM_TYPE,SUM(RESERV_PRICE)
            FROM RESERVATION
            JOIN ROOM USING(ROOM_NUM)
            JOIN ACCOMMODATION USING(ACM_NUM)
            WHERE TO_NUMBER(TO_CHAR(RESERV_DATE,'MM')) BETWEEN TO_NUMBER(TO_CHAR(ADD_MONTHS(SYSDATE,-3),'MM')) AND TO_NUMBER(TO_CHAR(SYSDATE,'MM'))
            GROUP BY ACM_TYPE);


